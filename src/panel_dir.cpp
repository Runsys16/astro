#include "panel_dir.h"


//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
PanelDir::PanelDir(FileBrowser* p)
{
    pFB = p;
    PanelSimple();
    setExtraString( "PanelDir" );
}

//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
void PanelDir::wheelUp( int xm, int ym)
{
    //logf( (char*)"PanelDir::wheelUp(%d, %d)", xm, ym );
    pFB->scrollDir( 1 );
}
//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
void PanelDir::wheelDown( int xm, int ym)
{
    //logf( (char*)"PanelDir::wheelDown(%d, %d)", xm, ym );
    pFB->scrollDir( -1 );
}
//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
void PanelDir::releaseLeft( int x, int y )
{
    logf( (char*)"PanelDir::releaseLeft(%d,%d)", x, y );
    log_tab(true);

    pFB->isInsideDir(x, y);

    log_tab(false);
    logf( (char*)"PanelDir::releaseLeft(%d,%d)", x, y );
}
//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
Panel* PanelDir::isMouseOver( int x, int y )
{
	Panel* p = PanelSimple::isMouseOverRaw(x, y);
	if ( p == this )
	{
		int X = x - getX();
		int Y = y - getY();
		
		//logf( (char*)"PanelDir::isMouseOver(%d,%d)", X, Y );
		
		pFB->mouseOverDir(X, Y);
	}
	return p;
}
