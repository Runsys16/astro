#include "panel_dir.h"


//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
PanelDir::PanelDir(FileBrowser* p)
{
    pFB = p;
    PanelSimple();
}

//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
void PanelDir::wheelUp( int xm, int ym)
{
    //logf( (char*)"PanelDir::wheelUp(%d, %d)", xm, ym );
    pFB->scrollDir( 1 );
}
//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
void PanelDir::wheelDown( int xm, int ym)
{
    //logf( (char*)"PanelDir::wheelDown(%d, %d)", xm, ym );
    pFB->scrollDir( -1 );
}
//--------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------
void PanelDir::releaseLeft( int x, int y )
{
    logf( (char*)"PanelDir::releaseLeft(%d,%d)", x, y );
    log_tab(true);

    pFB->isInsideDir(x, y);

    log_tab(false);
    logf( (char*)"PanelDir::releaseLeft(%d,%d)", x, y );
}


